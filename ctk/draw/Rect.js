ctk.Rect = function(X, Y, W, H)
{
	this.x = X || 0;
	this.y = Y || 0;
	this.w = W || 0;
	this.h = H || 0;


	//=====================================================================
	this.x2 = function()
	{
		return (this.x + (this.w - 1));
	};


	//=====================================================================
	this.y2 = function()
	{
		return (this.y + (this.h - 1));
	};


	//=====================================================================
	this.xy = function()
	{
		return new ctk.Point(this.x, this.y);
	};


	//=====================================================================
	this.wh = function()
	{
		return new ctk.Point(this.w, this.h);
	};


	//=====================================================================
	this.Clone = function()
	{
		return new ctk.Rect(this.x, this.y, this.w, this.h);
	};


	//=====================================================================
	this.Inflate = function(Amount)
	{
		this.x -= Amount;
		this.y -= Amount;
		this.w += (2 * Amount);
		this.h += (2 * Amount);
		return this;
	};


	//=====================================================================
	this.Deflate = function(Amount)
	{
		this.Inflate(-Amount);
		return this;
	};


	//=====================================================================
	this.MoveXY = function(X, Y)
	{
		this.x = X;
		this.y = Y;
		return this;
	};


	//---------------------------------------------------------------------
	this.MoveTo = function(X, Y)
	{
		this.MoveXY(X, Y);
		return this;
	};


	//=====================================================================
	this.OffsetXY = function(X, Y)
	{
		this.x += X;
		this.y += Y;
		return this;
	};


	//---------------------------------------------------------------------
	this.OffsetBy = function(X, Y)
	{
		this.OffsetXY(X, Y);
		return this;
	};


	//=====================================================================
	this.SetSize = function(W, H)
	{
		this.w = W;
		this.h = H;
		return this;
	};


	return this;
};
